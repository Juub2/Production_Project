package com.project.factory.resource.sub;

public class Delivery {
	
	private String time;
	private String id;
	private String name;
	private String birth;
	private String department;
	private String rank;
	private String startTime;
	private String goToWork;
	private String city;
	
	
	public Delivery(String time, String id, String name, String birth, String department, String rank, String startTime, String goToWork, String city) {
		
		this.time = time;
		this.id = id;
		this.name = name;
		this.birth = birth;
		this.department = department;
		this.rank = rank;
		this.startTime = startTime;
		this.goToWork = goToWork;
		this.city = city;
		
		
	}
	
}
